<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/vue3';

const props = defineProps({
    href: {
        type: String,
        required: true,
    },
    active: {
        type: Boolean,
        default: false, // Ensure a default value
    },
});

const classes = computed(() =>
    props.active
        ? 'block w-full ps-3 pe-4 py-2 border-l-4 border-indigo-400 text-start text-base font-medium text-indigo-700 bg-indigo-50 focus:outline-none focus:text-indigo-800 focus:bg-indigo-100 focus:border-indigo-700 transition duration-150 ease-in-out ' +
          'dark:border-indigo-600 dark:text-indigo-300 dark:bg-indigo-900/50' // Active state classes for responsive
        : 'block w-full ps-3 pe-4 py-2 border-l-4 border-transparent text-start text-base font-medium text-gray-600 hover:text-gray-800 hover:bg-gray-50 hover:border-gray-300 focus:outline-none focus:text-gray-800 focus:bg-gray-50 focus:border-gray-300 transition duration-150 ease-in-out ' +
          'dark:text-gray-300 dark:hover:text-gray-100 dark:hover:bg-gray-700 dark:hover:border-gray-600' // Inactive state classes for responsive
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
